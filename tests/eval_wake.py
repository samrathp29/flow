JUDGE_MODEL = "gpt-4.1"
PASS_THRESHOLD = 3.5

RUBRIC_PROMPT = """\
You are evaluating a developer re-entry briefing generated by a CLI tool.
The briefing is shown to a developer returning to a project after time away.

You will be given:
1. A description of the coding session(s) that occurred
2. The briefing the tool generated

Score the briefing on four dimensions (1-5 each):

**Relevance** (1-5): Does the briefing cover what was actually worked on?
  1 = Completely off-topic or empty
  3 = Mentions the right area but misses key details
  5 = Accurately captures the core work done

**Actionability** (1-5): Does it tell the developer what to do next?
  1 = No next step mentioned
  3 = Vague direction ("continue working on it")
  5 = Specific, concrete next action

**Conciseness** (1-5): Is it tight without fluff?
  1 = Wall of text or single unhelpful sentence
  3 = Reasonable length but some filler
  5 = Every sentence adds value, 3-5 sentences

**Accuracy** (1-5): Does it avoid hallucinating facts not in the session?
  1 = Invents details not present in the session
  3 = Mostly accurate with minor embellishments
  5 = Everything stated is grounded in the session data

Return ONLY a JSON object with keys: relevance, actionability, conciseness, accuracy.
Example: {"relevance": 4, "actionability": 3, "conciseness": 5, "accuracy": 4}"""


