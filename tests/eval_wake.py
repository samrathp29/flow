# Main
# ---------------------------------------------------------------------------

def main():
    # Load .env if it exists in the same directory
    env_path = Path(__file__).parent / ".env"
    if env_path.exists():
        for line in env_path.read_text().splitlines():
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                key, value = line.split("=", 1)
                os.environ.setdefault(key.strip(), value.strip().strip('"').strip("'"))

    # Validate env
    openai_key = os.environ.get("OPENAI_API_KEY")
    if not openai_key:
        print("Error: OPENAI_API_KEY env var required (for the judge LLM).")
        sys.exit(1)

    try:
        base_config = FlowConfig.load()
    except Exception as e:
        print(f"Error: Could not load flow config: {e}")
        print("Run `flow init` first.")
        sys.exit(1)

    judge_client = OpenAI(api_key=openai_key)

    print("=" * 70)
    print("FLOW WAKE EVALUATION")
    print("=" * 70)
    print(f"Judge model: {JUDGE_MODEL}")
    print(f"Flow LLM:    {base_config.llm_provider}/{base_config.llm_model}")
    print(f"Scenarios:   {len(SCENARIOS)}")
    print(f"Pass threshold: avg >= {PASS_THRESHOLD}")
    print("=" * 70)

    results = []

    for i, scenario in enumerate(SCENARIOS, 1):
        print(f"\n{'─' * 70}")
        print(f"Scenario {i}/{len(SCENARIOS)}: {scenario['name']}")
        print(f"{'─' * 70}")

        # Isolated data dir per scenario
        data_dir = Path(tempfile.mkdtemp(prefix="flow-eval-"))
        try:
            config = make_config_with_data_dir(data_dir)

            print("  Running pipeline...")
            briefing = run_scenario(scenario, config)
            print(f"\n  Briefing:\n  {briefing}\n")

            print("  Judging...")
            scores = judge_briefing(briefing, scenario, judge_client)
            avg = sum(scores.values()) / max(len(scores), 1)
            passed = avg >= PASS_THRESHOLD

            results.append({
                "name": scenario["name"],
                "scores": scores,
                "avg": avg,
                "passed": passed,
                "briefing": briefing,
            })

            status = "PASS" if passed else "FAIL"
            print(f"  Scores: {scores}")
            print(f"  Average: {avg:.1f} [{status}]")

        finally:
            shutil.rmtree(data_dir, ignore_errors=True)

    # Summary table
    print(f"\n{'=' * 70}")
    print("RESULTS SUMMARY")
    print(f"{'=' * 70}")
    print(f"{'Scenario':<45} {'Rel':>4} {'Act':>4} {'Con':>4} {'Acc':>4} {'Avg':>5} {'':>5}")
    print(f"{'─' * 45} {'─' * 4} {'─' * 4} {'─' * 4} {'─' * 4} {'─' * 5} {'─' * 5}")

    all_passed = True
    for r in results:
        s = r["scores"]
        status = "PASS" if r["passed"] else "FAIL"
        if not r["passed"]:
            all_passed = False
        print(
            f"{r['name']:<45} "
            f"{s.get('relevance', 0):>4} "
            f"{s.get('actionability', 0):>4} "
            f"{s.get('conciseness', 0):>4} "
            f"{s.get('accuracy', 0):>4} "
            f"{r['avg']:>5.1f} "
            f"{status:>5}"
        )

    print(f"{'─' * 45} {'─' * 4} {'─' * 4} {'─' * 4} {'─' * 4} {'─' * 5} {'─' * 5}")

    overall_avg = sum(r["avg"] for r in results) / max(len(results), 1)
    overall_status = "PASS" if all_passed else "FAIL"
    print(f"{'Overall':<45} {'':>4} {'':>4} {'':>4} {'':>4} {overall_avg:>5.1f} {overall_status:>5}")

    print(f"\n{'=' * 70}")
    if all_passed:
        print("All scenarios passed.")
    else:
        failed = [r["name"] for r in results if not r["passed"]]
        print(f"Failed scenarios: {', '.join(failed)}")

    sys.exit(0 if all_passed else 1)


if __name__ == "__main__":
    main()
