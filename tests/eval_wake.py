# ---------------------------------------------------------------------------
# Pipeline helpers
# ---------------------------------------------------------------------------

def make_config_with_data_dir(data_dir: Path) -> FlowConfig:
    """Load user's flow config but override data_dir for isolation."""
    config = FlowConfig.load()
    return replace(config, data_dir=data_dir)


def run_scenario(scenario: dict, config: FlowConfig) -> str:
    """Run a scenario through the real pipeline, return the wake briefing."""
    project_name = "eval-project"
    project_path = "/tmp/eval-project"

    # Use a single FlowMemory instance for the entire scenario to avoid
    # Qdrant file lock conflicts (mem0's internal telemetry store).
    mem = FlowMemory(config)

    for i, session in enumerate(scenario["sessions"]):
        data = RawSessionData(
            project_name=project_name,
            project_path=project_path,
            started_at=session["started_at"],
            ended_at=session["ended_at"],
            duration_mins=session["duration_mins"],
            turns=session.get("turns", []),
            git_diff=session.get("git_diff", ""),
            git_log=session.get("git_log", ""),
        )

        chunks = Formatter().format(data)
        metadata = {
            "session_date": session["started_at"][:10],
            "duration_mins": session["duration_mins"],
        }
        mem.add_chunks(chunks, project_name, metadata)

    # Reuse the same FlowMemory for retrieval (avoids Qdrant lock conflict)
    retriever = Retriever.__new__(Retriever)
    retriever.flow_memory = mem
    retriever.llm = LLM(config)

    last_session = scenario["sessions"][-1]
    mock_git_log = last_session.get("git_log", "")

    with patch.object(Retriever, "_git_log", return_value=mock_git_log), \
         patch.object(Retriever, "_days_since_last_session", return_value=7):
        briefing = retriever.wake(project_name, project_path)

    mem.close()
    return briefing


# ---------------------------------------------------------------------------
# Judge
# ---------------------------------------------------------------------------

def judge_briefing(
    briefing: str,
    scenario: dict,
    client: OpenAI,
) -> dict:
    """Score a briefing using the LLM judge. Returns score dict."""
    user_content = (
        f"## Session Description\n{scenario['description']}\n\n"
        f"## Generated Briefing\n{briefing}"
    )

    response = client.chat.completions.create(
        model=JUDGE_MODEL,
        messages=[
            {"role": "system", "content": RUBRIC_PROMPT},
            {"role": "user", "content": user_content},
        ],
        temperature=0.0,
        response_format={"type": "json_object"},
    )

    raw = response.choices[0].message.content
    try:
        scores = json.loads(raw)
    except json.JSONDecodeError:
        print(f"  [!] Judge returned invalid JSON: {raw}")
        scores = {"relevance": 0, "actionability": 0, "conciseness": 0, "accuracy": 0}

    return scores


