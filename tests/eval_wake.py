"""
Evaluation script for flow wake briefing quality.

Runs synthetic coding sessions through the real pipeline
(Formatter → mem0 → Retriever), captures wake briefings,
and scores them with an OpenAI LLM judge against a rubric.

Usage:
    OPENAI_API_KEY=sk-... python tests/eval_wake.py

Requires:
    - ~/.config/flow/config.toml with valid LLM config
    - OPENAI_API_KEY env var (for the judge, separate from flow's LLM)
"""

